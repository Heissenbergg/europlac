        <h3>Šta je građevinska dozvola?</h3>
        <p>
            Građevinska dozvola je dokument na temelju kojeg se može započeti gradnja građevine. 
            Njome se utvrđuje da je glavni idejni projekat izrađen u skladu sa propisima i utvrđenim 
            uvjetima koje mora ispunjavati građevina na određenoj lokaciji te da su ispunjeni svi potrebni preduvjeti za gradnju. 
            Za one koji bi htjeli da sagrade vlastitu kuću, a nemaju dovoljno vremena ni dovoljno informacija o 
            procesu nabavljanja građevinske dozvole, nudimo vam rješenje.
        </p>
